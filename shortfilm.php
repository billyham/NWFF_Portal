            <h2>Description</h2>
            <p>
                The films produced are defined by their shared constraint: they are 
                "one shot" films. The format draws inspiration from a long-standing 
                narrative technique in filmmaking, of using long, uninterrupted takes.  
                To name just a few, directors from BÃ©la Tarr and Alexander Sukurov to 
                Alfred Hitchcock and Martin Scorcese have all used long takes within 
                feature films to notable effect. 
            </p>
            
            <p>
                Our program asks filmmakers to channel the creative possibilities of this 
                format by creating an entire short film from a single long take. With 
                the exception of credits, the film must be photographed in a single shot 
                with no edits. Multiple takes are allowed and encouraged, and in the editing, 
                the starting and ending point are entirely the decision of the director, but 
                the final film must be one continuous shot. There are no constraints to the 
                sound design, and need not include live sound from the source footage. These 
                rules are open to broad interpretation (for example, a filmmaker shot a 
                time-lapse film as his one-shot). 
            </p>
            
            <p>

                The chosen filmmakers will receive a $500 cash budget for the film's production. 
                As well as in-kind services from NWFF: 6 days of free use of a NWFF camera  
                (Panasonic HVX200, Canon 60D, Aaton Super 16mm or Eclair 16mm), a light kit 
                and microphone, and up to 40 hours of edit suite time. Further informal 
                services to the extent that NWFF staff can provide including recommendations 
                for cast, crew, locations and additional production resources.
            </p>
            
            <p>

                The films will be screened at NWFF. They will make their premiere at NWFF's 
                Local Sightings Festival 2011 and go on to play at intervals appropriate to 
                the programming schedule of the cinemas. Efforts will be made to have the films 
                play in other regional theaters and festivals worldwide. The artists retain 
                ownership of their films and are free to pursue their own distribution and 
                exhibition opportunities.  
            </p>
            
            <p>

                The films will be included among a catalog of one-shot short films that were 
                produced and are currently distributed by NWFF. A body of work that already 
                includes filmmakers Aaron Katz, Andrew Bujalski, Joe Swanberg, Todd Rohal, 
                Barry Jenkins, Lisandro Alonso, Curtis Winter, Amie Siegel and Josh and Benny 
                Safdie. 
            </p>


            <h2>Eligibility:</h2>

            <p>
                To be eligible for consideration, the production, as represented by the 
                Project Director or the Project Producer must:

            <ul>1. Maintain active NWFF membership.</ul>
            <ul>2. Demonstrate a clear commitment to the production, which must be non-commercial 
                in nature and must contribute artistically to the local film community.</ul>
            <ul>3. Present a detailed proposal, following proposal guidelines.</ul>
            <ul>4. Be a Washington State resident.</ul>
        </p>



