        <p>
            We will host established and emerging artists for intensive 72-hour institutes 
            that include one-on-one story conferences, workshops, seminars, and mentoring 
            sessions meant to transform emerging filmmakers’ careers. Our location is a 
            grand house on nearby Orcas Island, far enough from the city to insulate 
            creativity. The concept is simple, and it is adapted from the Sundance Institute 
            model: we will invite four or five filmmakers of national (and international) 
            reputation to mentor and teach four or five regional filmmakers of emerging 
            reputation. The filmmakers we invite and select will be new voices, envelope-pushing 
            talents. Using this as a first step we will, over a couple of years, further 
            develop the three-day institutes into longer and multi-branched residencies, 
            in which artists will be invited to create new work over longer periods. These 
            future residencies will include teaching, the mentoring of younger filmmakers, 
            and film curation.
        </p>

