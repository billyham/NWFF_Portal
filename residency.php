            and visiting artists, including weeklong film premieres, retrospectives, master 
            classes, and short film production. In 2010, we hosted over 80 guest filmmakers, 
            including many artists we invited to stay for several days to interact with our 
            audiences and with local filmmakers.
        </p>
            
        <p>
            The selection process will include inviting applications from emerging regional 
            filmmakers, to include a script or detailed treatment and a sample reel. We will 
            allow two-person writer-director teams to participate as well.
        </p>
        
